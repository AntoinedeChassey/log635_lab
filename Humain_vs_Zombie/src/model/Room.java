package model;

public class Room {
	private Integer id_room;
	private Integer id_item;
	private Integer size;
	private Integer light;
	
	public Room(Integer id_room, Integer id_item, Integer size, Integer light) {
		super();
		this.id_room = id_room;
		this.id_item = id_item;
		this.size = size;
		this.light = light;
	}

	
	
}
