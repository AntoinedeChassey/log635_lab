package model;

public class Zombie {

	private Integer id_zombie;
	private String name;
	private Integer id_room;
	private Integer aggressivity;
	private Integer size;
	
	public Zombie(Integer id_zombie, String name, Integer id_room, Integer aggressivity, Integer size) {
		super();
		this.id_zombie = id_zombie;
		this.name = name;
		this.id_room = id_room;
		this.aggressivity = aggressivity;
		this.size = size;
	}
	
	
	

}
